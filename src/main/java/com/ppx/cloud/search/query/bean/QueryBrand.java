package com.ppx.cloud.search.query.bean;

public class QueryBrand {
	
	private Integer bid;
	
	private String bn;
	
	private Integer n;
	
	public QueryBrand() {
		
	}
	
	public QueryBrand(Integer bid, Integer n) {
		this.bid = bid;
		this.n = n;
	}

	public Integer getBid() {
		return bid;
	}

	public void setBid(Integer bid) {
		this.bid = bid;
	}

	public String getBn() {
		return bn;
	}

	public void setBn(String bn) {
		this.bn = bn;
	}

	public Integer getN() {
		return n;
	}

	public void setN(Integer n) {
		this.n = n;
	}
	
	
	
	
	
	
	
}

























