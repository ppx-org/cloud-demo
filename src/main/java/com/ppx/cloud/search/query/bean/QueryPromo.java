package com.ppx.cloud.search.query.bean;

public class QueryPromo {
	
	private Integer gid;
	
	private String gN;
	
	private Integer n;
	
	public QueryPromo() {
		
	}
	
	public QueryPromo(Integer gid, Integer n) {
		this.gid = gid;
		this.n = n;
	}
	
	public Integer getGid() {
		return gid;
	}

	public void setGid(Integer gid) {
		this.gid = gid;
	}
	
	public String getgN() {
		return gN;
	}

	public void setgN(String gN) {
		this.gN = gN;
	}

	public Integer getN() {
		return n;
	}

	public void setN(Integer n) {
		this.n = n;
	}
	
	
	
	
	
	
}

























