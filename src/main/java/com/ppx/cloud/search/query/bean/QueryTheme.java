package com.ppx.cloud.search.query.bean;

public class QueryTheme {
	
	private Integer tid;
	
	private String tn;
	
	private Integer n;
	
	public QueryTheme() {
		
	}
	
	public QueryTheme(Integer tid, Integer n) {
		this.tid = tid;
		this.n = n;
	}

	public Integer getTid() {
		return tid;
	}

	public void setTid(Integer tid) {
		this.tid = tid;
	}

	public String getTn() {
		return tn;
	}

	public void setTn(String tn) {
		this.tn = tn;
	}

	public Integer getN() {
		return n;
	}

	public void setN(Integer n) {
		this.n = n;
	}

	
	
	
	
	
	
}

























