package com.ppx.cloud.store.promo.program.bean;

public class ProgramChange {

	private Integer progId;

	private Integer prodId;

	private Float enoughPrice;
	
	private Float changePrice;

	public Integer getProgId() {
		return progId;
	}

	public void setProgId(Integer progId) {
		this.progId = progId;
	}

	public Integer getProdId() {
		return prodId;
	}

	public void setProdId(Integer prodId) {
		this.prodId = prodId;
	}

	public Float getEnoughPrice() {
		return enoughPrice;
	}

	public void setEnoughPrice(Float enoughPrice) {
		this.enoughPrice = enoughPrice;
	}

	public Float getChangePrice() {
		return changePrice;
	}

	public void setChangePrice(Float changePrice) {
		this.changePrice = changePrice;
	}

	
	

	

}
